function RHOV_air = calculateAirWaterVapor(Tm, RHa)
% 计算大气水蒸气参数
%from : 一种大气中水蒸气密度快速计算方法,邵翔宇,2022
% 输入：
%   T_K - 环境温度（开尔文）
%   phi - 相对湿度（%RH）
% 输出：
%   p_s - 饱和水蒸气压力（hPa）
%   p_H20 - 水蒸气分压力（kPa）
%   rho_v - 水蒸气密度（kg/m³）

T1 = 273.16; % 水三相点温度（K）
R_v = 8314; % 水蒸气气体常数（J/(kmol·K)）
M_H2o = 18.015;%水的摩尔质量（kg/kmol）
% === 计算饱和水蒸气压力 p_s (公式1) ===

term1 = 10.79574 * (1 - T1./(Tm+273.16));
term2 = -5.02800 * log10((Tm+273.16)./T1);
term3 = 1.50475e-04 * (1 - 10.^(-8.2969*((Tm+273.16)/T1-1)));
term4 = 0.42873e-03*10^(4.76955 * (1 - (T1./(Tm+273.16)))-1); % 修正了绝对值内的表达式
term5 = 0.78614;

% 计算常用对数并转换为实际压力
lg_p_s = term1 + term2 + term3 + term4 + term5;
p_s = 10.^lg_p_s; % 单位：hPa

% === 计算水蒸气分压力 p_H20 (公式2) ===
% 注意单位转换：公式输出为kPa
p_H20 = 0.1 .* (RHa./100) .* p_s; % 单位：kPa（已添加phi/100转换）

% === 计算水蒸气密度 rho_v ===
% 统一单位到Pa和K计算密度
p_H20_Pa = p_H20 * 1000 * M_H2o; % 转换为Pa
RHOV_air =0.001*p_H20_Pa ./ (R_v .* (Tm+273.16)); % 单位：g/cm³
end